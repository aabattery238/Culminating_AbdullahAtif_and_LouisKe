    private boolean loginPassword = false;
    private String usernameLogin;
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (loginPassword) {
            String passwordInput = ErrorCheck.stringParse(txfLoginPassword.getText(), 10, "Password", this);
            if (passwordInput == null) {
                return;
            }
            User loggedInUser = User.login(usernameLogin, passwordInput, this);
            if (loggedInUser == null) {
                return;
            } else {
                //Create new remove employees screen
                HomeScreenGUI homeScreenGUI = new HomeScreenGUI(loggedInUser);
                homeScreenGUI.setVisible(true);
                //Remove this screen
                this.dispose();
            }
        } else {
            String usernameInput = ErrorCheck.trueStringParse(txfLoginUsername.getText(), 10, "Username", this);
            if (usernameInput == null) {
                return;
            } else if (User.checkUserExists(usernameInput, this)) {
                loginPassword = true;
                lblLoginPassword.setVisible(true);
                txfLoginPassword.setVisible(true);
                usernameLogin = usernameInput;
            } else {
                JOptionPane.showMessageDialog(this, "User Not Found", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } 
    }//GEN-LAST:event_btnLoginActionPerformed

    private void txfLoginUsernameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfLoginUsernameKeyPressed
        if (loginPassword) {
            txfLoginPassword.setText("");
            lblLoginPassword.setVisible(false);
            txfLoginPassword.setVisible(false);
            loginPassword = false;
        }
    }//GEN-LAST:event_txfLoginUsernameKeyPressed

    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        String usernameInput = ErrorCheck.trueStringParse(txfSignUpUsername.getText(), 10, "Username", this);
        if (usernameInput == null) {
            return;
        }
        String passwordInput = ErrorCheck.stringParse(txfSignUpPassword.getText(), 10, "Password", this);
        if (passwordInput == null) {
            return;
        }
        User.signUp(usernameInput, passwordInput, this);
        jTabbedPane.setSelectedIndex(0);
    }//GEN-LAST:event_btnSignUpActionPerformed

